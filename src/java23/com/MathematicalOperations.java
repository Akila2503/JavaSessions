package java23.com;

public class MathematicalOperations {

	public static void main(String[] args) {
		
		                  //Mathematical Operations
		
		    System.out.println(10/2);
	        System.out.println(6+9);
	        System.out.println(5-10);
	        System.out.println(9*4);
	        System.out.println(63/7);
	        
	        System.out.println(22.2/2);
	        System.out.println(12.5/2.5);
	        System.out.println(10.0/2);
	        

	        System.out.println(10.0/0); //Infinity
	        System.out.println(44.1/0);   //Infinity
	        System.out.println(8.2/0.0);  //Infinity
	        
	        System.out.println('c'/2); //ASCII Value of c is 98
	        System.out.println('d'/2); //ASCII Value of d is 100
	        
	        System.out.println(0/3);  //0
	        System.out.println(0*5);  //0
	        System.out.println(0/2.5);  //0.0
	        
	        System.out.println(0.0/0.0); // Not a Number
	        System.out.println(0.0/0);   //NaN
	        System.out.println(0/0.0);   //NaN
	     
	        //System.out.println(9/0);   //ArithmeticException
	        //System.out.println(0/0);   //ArithmeticException
	        //System.out.println(2/0);   //ArithmeticException
	        

	}

}
